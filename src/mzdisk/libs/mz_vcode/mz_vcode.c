/** @copydoc mz_vcode_from_ascii */
uint8_t mz_vcode_from_ascii ( uint8_t ascii_code, en_MZ_VCODE_CHARSET charset ) {
    uint8_t vcode = adcn_eu[ascii_code];

    /* Korekce pro JP variantu (MZ-1500) - 2 odlišné pozice */
    if ( charset == MZ_VCODE_JP ) {
        if ( ascii_code == 0x6C ) return 0xE9;
        if ( ascii_code == 0x6D ) return 0xEA;
    }

    return vcode;
}


/** @copydoc mz_vcode_from_ascii_dump */
uint8_t mz_vcode_from_ascii_dump ( uint8_t ascii_code, en_MZ_VCODE_CHARSET charset ) {
    /* Řídící znaky nahradit tečkou (stejně jako příkaz D v monitoru) */
    if ( ascii_code < 0x20 ) {
        ascii_code = 0x2E;
    }
    return mz_vcode_from_ascii ( ascii_code, charset );
}


